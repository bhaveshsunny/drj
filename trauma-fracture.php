<h4>Trauma and Fractures</h4> 

<p>Orthopedic trauma is a severe injury of musculoskeletal system which is caused by a sudden accident requiring immediate medical attention. Orthopedic trauma condition refers to fractures and dislocations caused by traumatic events. Not all orthopedic traumas are life-threatening, it is life altering. Therefore, the choice of doctors is extremely important. Orthopedic trauma doctors are specialized in complex surgeries of bones, joints and soft tissues to the entire body. Many specialists are just to treat one part of body while others are into general care, but they cannot treat more acute fractures. However, they receive training in orthopedic surgery with a focus on fractured bones treatment and joint realignment.</p>

<p>Some of the common orthopedic traumas are femoral and tibial shaft fractures, acetabular and pelvic fractures, spine and spinal cord injuries, hand and upper extremity injuries, foot and ankle injuries. Simple fractures, unstable fractures, open (compound) fractures, greenstick fractures are some types of fractures which are commonly noticed. </p>

<p>According to the severity of fracture, specialists will decide on the type of treatment given. Open and closed treatments are two wide categories of types of methods. Open treatment involves surgical dealings with broken bone ends to fix them in place. Close treatment is a method where practitioner applies gentle pressure on the bone and with the skin and muscles left intact. Hence, no incursions are made or the bone is not seen by the surgeon. Closed methods are rarely implemented on the patients because the success rate is low when compared to surgical treatments.</p>

