								<h4>Arthroscopy & Sports Injuries:</h4> 
								<p>Arthroscopic surgery is a procedure that allows surgeons to visualize, diagnose, and treat joint problems. The name is derived from the Greek words arthron ,( joint) and skopein ( to look at) Arthroscopy is performed using an arthroscopy, a small fiber-optic instrument that enables a close look at the inside of a joint through a small key hole incision, , where interior of the joint can be visualized through a telescope and the whole procedure can be seen on a TV monitor.</p>
								
                              <p>Before the introduction of optical and fiber optic equipment in orthopedics , surgery on the inside of any joint meant making a large incision and opening the joint to do even the most minor procedure. Now its changing and today it is possible to do many surgeries through arthroscopy.</p>
							  
								 
								<p>The accuracy of arthroscopy is said to be 100% for diagnosis compared to diagnostic imaging such as MRI in diagnosing damage to the bones, cartilage, tendons, ligaments and muscles that make up a joint. Arthroscopic surgery may be used to relieve mechanical joint problems, such as buckling, stiffness, or locking, and can preclude or delay the need for more aggressive surgery such as a joint replacement.</p>
