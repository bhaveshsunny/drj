								<p>There are several over-the-counter (non prescription) medicines  that are good for innumerable types of pain. Discrete attention to pain management is a critical component of a patient's recovery, as acute or chronic low back pain leading to depression, difficulty  in sleeping, and difficulty  to exercise and stretching, which in turn can worsen  and prolong a painful back condition.  There are two main types of OTC pain medicines: acetaminophen (Tylenol) and nonsteroidal anti-inflammatory drugs (NSAIDs). Aspirin, naproxen (Aleve), and ibuprofen (Advil, Motrin) are examples of OTC NSAIDs. If OTC medicines doesn't solace your pain, your physician  might prescribe a  stronger medicine dosage such as NSAIDs. The most powerful pain soothers are opioids. They are very effective, but have the risk of  serious side effects. There is  also a risk getting addicted to the medicines to subdue the pain. Analysing the  risks, you must use them only under a doctor's supervision. Because of the potential side effects, risks, and complications of opioids, and the body's increasing tolerance to opioids over time, these medications are most clearly indicated for treatment of short-term intense pain, such as acute postoperative pain. If used to treat pain for longer periods, close monitoring is essential.</p>
							
