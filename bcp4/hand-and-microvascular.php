								<h4>Hand & Microvascular Surgery</h4> 
								<p>The Hand & Microvascular Surgery department is dedicated to treatment of disorders relating to the hand and wrist. The team specialises in dealing traumatic and non-traumatic conditions related to the hand. With a multimodal method the department comprises of hand surgeons qualified in several fields regarding hand surgery including trauma, Nerve repairs, tendon transfer surgeries, congenital anomalies, brachial plexus injuries, carpal bone fractures and wrist arthroscopy. Treatment of hand injuries including reimplantations, reconstruction, and limb salvage surgery is normally accomplished. The department objective is to reestablish the aesthetic and functional value of hand. A devoted team of hand physiotherapists  examine the rehabilitated patient post surgery. </p>
								<p>The department specializes in the physiotherapy of hand fractures, hand ligament reconstructions and repair, finger contracture release, elbow replacement, brachial plexus injury, tendon transfers, TFCC injuries, Flexor and extensor tendon repairs, carpal tunnel syndrome and crush hand injuries.</p>
                             
								<h4> Congenital Abnormalities</h4>
								<p>The most usual hand problems in infants are polydactyly (extra fingers) and syndactyly (webbed fingers). In all hand surgeries and especially pediatric hand surgeries, in India atraumatic techniques (causing no tissue damage) are used. This consists of treatment of all birth-related injuries, such as radial and ulnar club-hand, congenital absence of the thumb, cerebral palsy and syndactyly.</p>
                          
								<h4>Carpal Tunnel Syndrome</h4>
								<p>Carpal Tunnel Syndrome occurs most normally due to the compression of the median nerve at the wrist where it passes through a narrow area called the carpal tunnel. The symptoms contain numbness in the thumb, index, and middle fingers especially at night or upon waking up in the morning. Carpal Tunnel Syndrome can be treated by releasing the tightening through endoscopic or nominal access surgery.</p>
                          
								<h4>Arthritis</h4>
								<p>Reconstruction of painful arthritic deformities is performed routinely and includes tendon transfers, muscle release and joint displacement fusion. Reconstructive Surgery (for tumors, nerve compression and arthritis) Reconstructive hand surgery alleviates pain and helps correct deformities caused by tumors, nerve compression and arthritis. Many benign tumors of the hand and wrist don't always cause pain and subside soon. However, when pain persists or limits necessary function, removal is recommended and is almost universally curative.</p>
                          
								<h4>Microsurgery (for finger deformities)</h4>
								<p>Replantation of a single finger / multiple fingers can be done through microsurgery. Even if multiple finger amputation has been done, one or more fingers can be replanted in a position that will allow functioning of the hand. Replantation surgery will be followed by rehabilitative therapy for the hand.</p>
                         
								<h4>Microvascular Repair and Reconstruction</h4>
								<p>This treatment performs microsurgical procedures for patients and today is one of the few hospitals in India that provide that service on a regular basis. Human Touch Hospital has experienced team of surgeons who provide prompt patient evaluation for acute traumatic injury for the most intricate surgical needs.</p>
                            
								<h4>Brachial Plexus and Peripheral Nerve Injuries </h4>
								<p>This treatment includes the evaluation and management of both adult and pediatric patients with traumatic and birth-related brachial plexus injuries as well as the treatment of acute and chronic peripheral nerve injuries.</p>
                              
								<h4>Dupuytrens Disease</h4>
								<p>Dupuytrens disease is a thickening of the palmar fascial tissue that causes the fingers to bend towards the palm. It is characterized by nodular or cordlike thickening of the fascia (lining tissue) of the palm of the hand and can be associated with inability to straighten the fingers.</p>
                                
								<h4>Trigger Finger</h4>
								<p>Trigger finger is a common condition of the hand that is characterized by catching or locking of the fingers when they are bent to the palm. While the ring finger is the most commonly affected finger, digital triggering can occur in any of fingers. Often the symptoms are worse in the evening and the condition is more common in patients with diabetes.</p>  
                                 
								<h4>Trauma </h4>
								<p>This includes treatment of fractures and dislocations of the upper extremity, flexor and extensor tendon injuries, nerve injuries, compartment syndromes, and replantation/microvascular emergency services. Experience is obtained for acute, sub-acute and chronic injury presentation.</p>  
                                   
								<h4>Free-tissue transfer </h4>    
								<p>This includes our significant experience with free, vascularized fibular grafting, soft tissue reconstruction with local, regional, and distant tissue transfer. </p>       
