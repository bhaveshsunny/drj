								<p>General Orthopedics  is a medical practise done by the physicians who specalize in    treatment of patients with disarrangement of the bones, joints, muscles, ligaments, tendons, nerves,ruptured disks. These elements comprise the musculoskeletal system. The physicians who specialize in this area are called orthopaedic surgeons or orthopaedic surgeons. Orthopaedics surgeons deal  with medical specialty that primarily focuses on injuries, diagnosis and treatment of  diseases of body's musculoskeletal system. The complex system comprises of elements including disorder of hammer toes, arthritis, osteoporosis,club foot and unequal leg length,abnormalities of the fingers and toes and growth abnormalities.</p>
							
								<p>Orthopaedic surgeons supervise peculiar  problems of the musculoskeletal system without surgery, through advanced imaging procedures, such as MRI excercises and rehabilitative therapies. Most orthopaedic disorders do not require surgery. The best approach is to converse with a musculoskeletal specialist who can initiate a customized treatment plan. These experts know when it is appropriate to involve other members of the team, including when there is a need for additional consultation and/or surgery. </p>
							
