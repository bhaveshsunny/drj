                                <h4>Knee Revision Surgery</h4>
                                <p>Knee revision surgery, also called revision Total Knee Arthroplasty, is a surgical procedure where the previously implanted artificial knee joint, or prosthesis is replaced with a new prosthesis. Knee revision surgery also associates usage of  bone grafts. The bone graft might be an autograft, meaning that the bone is taken from another area of the patient's body; or an allograft, in the sense that the bone tissue is taken from a some other donor.</p>

                                <h4>Reasons for Knee Revision Surgery</h4>
                                <p>A knee joint might fail due to the below reasons:</p>

                                <p>Aseptic Loosening</p>
								<p>Wearing of plastic</p>
								<p>Improper alignment</p>
								<p>Unstable Joint</p>


							 <h4>Various categories of  Knee Replacement surgeries</h4>

							 <p>There are basically two kinds of Knee replacement surgeries namely the  TKR (Total Knee Replacement) and UKR (Unicondylar / Partial Knee Replacement). Knee Replacement is a  surgery that’s done as a part of the knee or done only over a partial area or a total knee replacement. Typically, the surgery removes the damaged joint  and the surface is replaced with either metal  or  plastic surface which allows ease of movement in the  knee joint area. It’s also called as Implant or Prosthesis.</p>

							 <h4>Partial Knee Replacement (PKR)</h4>
                              
                              <p>Partial knee replacement, also referred to as ‘unicompartmental knee Arthroplasty or unicondylar knee Arthroplasty’ is a surgical procedure  which targets the  treatment of osteoarthritis of the knee joint where the entire knee joint is not  replaced. Usually patients  opt  for  total knee replacement  when there is  severe arthritis in the knee joint. In a total knee replacement, the complete cartilage is removed from the knee joint, and a single metal implant is  replaced. </p>

                              <h4>Conditions for Partial Knee Replacement</h4>
                              <p>The partial knee surgery may be possible if the arthritis in the knee is confined to a limited area. If the arthritis is widespread, then the partial knee replacement is not the best option.</p>

                              <h4>Total Knee Replacement (TKR)</h4>
                              <p>In a Total Knee Replacement procedure, the surgeon removes the  damaged or the defective  cartilage & bone  from  the knee area &  it’s either replaced with a metal or  plastic. In Total Knee Replacement, three areas of the knee are replaced for minimizing pain and bringing mobility.  The two areas of the prosthesis are placed onto the ends of the femur, tibia, and undersurface of the patella using a special bone cement. Usually, metal is used at the end of the femur, while plastic is used on the tibia  region & patella for the new knee surface. Recently surgeons are using skin friendly surfaces such as metal on metal or ceramic on ceramic.</p>

                              <h4>Situations when TKR comes to rescue</h4>
                              <p>Activities such as  walking, climbing stairs and getting in and out of chairs are restricted, hence rest is advised. Restricted knee function or mobility, such as chronic knee stiffness and swelling that restricts an individual from stretching or bending in the knee area. When other approaches have failed to provide relief such as resting, weight loss, physical therapy, support of other walking aid, medications, braces and surgery which have failed to relieve the pain..</p>

