								<h5><strong>CT-Scan: </strong></h5>
								<p>Computerized tomography (CT-Scan) is a specific type of X-ray that takes cross-sectional images of different parts of the body images are examined by the technician. Sometimes CT-images are merged to create 3-D images to provide a clear idea of the tumor. Doctors recommend CT-scan to identify internal bleeding and internal swellings, to spot the area of the tumor. In some cases, a contrast material is used for CT-scan that allows highlighting the area of the body that is to be tested. This contrast material enters into our bodies through various ways such as oral, injection and rectal. </p>
                      
								<h5>Brain tumors: </h5>
								<p>These tumors are serious disorders that cause more pressure on the brain or even damage the brain. The main cause for raise in intracranial pressure is due to brain tumor. The advancement in various surgical methods like anesthesia, micro neurological techniques have illuminated patient’s disorders related to the brain tumor. These can be occurred due to tuberculoma, abscess, cysticercosis, AIDS etc. The symptoms of brain tumor depend on the location of the tumor. So, there are chances for increasing paralysis, memory loss, speech loss or loss of body coordination. For the confirmation of brain tumor, neurologists perform various treatments such as CT Scan brain, MRI (Magnetic Resonance Imaging) and lumbar puncture.</p>
