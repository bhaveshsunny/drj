								<h4>TYPES OF DIABETES</h4>                                                                                                                                                                
								<p>Type 1 diabetes,  your body doesnt produce insulin as the beta cells in their pancreas ae destroyed. Such people use insulin injections to allow the body to process glucose and avoid complications from hyperglycemia.</p>                                                                                                                                                                                                                                                  
								<p> Type 2 diabetes does not respond well to insulin., your body does not produce or utislize insulin well. Without enough insulin, the glucose is retained in your blood.</p>                                                                                                                                                                                                                                                                                                                                                                                                                 
							                       
							
								<p>People with type 2 diabetes do not respond well or are resistant to insulin.They are first treated with oral medications, with specific diet and excercises. </p>
							
