<h4>Stem cell Therapy</h4>

<p>Painful Joints and discs in the neck or low back are common causes of severe disability and back pain. Historically, therapies did not exist to regenerate the degenerative process in a vertebral disc, often leaving surgical intervention as the only option if other non-operative treatment options have failed. In selected patients, there are better ways to treat spinal and Joint disease. Regenerative therapies for the spine and Joints are the future for spinal and joint treatments. We are excited to offer innovative techniques as new and improved ways to try to heal spinal and joint problems without having to undergo surgery. Regenerative therapy options hold wonderful healing potential and represent the future of modern medicine.</p>
