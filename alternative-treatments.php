<h4>The Osteoarticulat Transfer System (OATS)</h4>
<p>The Osteoarticular Transfer System, commonly known as OATS, replaces damaged cartilage in the knee with healthy cartilage from another area of the joint, relieving pain and restoring movement and function to the joint. A mosaicplasty is the name for a general procedure that treats severe cartilage damage, and the OATS procedure is one type of mosaicplasty.</p>

<p>Although cartilage is essential to smooth, painless movement of the joints, some areas have a more critical need for the support and cushioning provided by the cartilage. During the OATS procedure, small plugs of healthy cartilage are removed from areas of the joint that are not in critical need, and transferred to the area of damaged cartilage.</p>

<p>The OATS procedure is ideal for patients with small areas of cartilage damage that can be easily repaired with a graft. Widespread cartilage damage cannot usually be treated with this procedure, since there may be insufficient amounts of healthy cartilage available.</p>

<p>After the OATS procedure, patients will need to undergo a lengthy physical therapy program in order to restore range of motion and relieve pain and swelling on the joint. Most patients will be on crutches for 6 to 12 weeks after surgery before they can successfully bear weight on the joint again. Long-term follow-up care will be required in order to maintain the results of this procedure.</p>

