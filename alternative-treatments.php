<h4>This is How It Works</h4>


<p>We obtain a patient’s own stem cells by aspirating tissue from the patient's hip bone or from their fat cells. These cells are centrifuged down to identify and separate specific primitive cells that will help heal tissues. Stem cells are then injected into the disc, stimulating healing of the disc by using these primitive blood cells to stimulate regeneration of the collagen within the disc. We are excited to report improvements in our patients treated with stem cells.</p>
<p>Not all patients will be a candidate for these disc regeneration procedures. For those whom are ideal candidates, this provides great hope with reduction in pain and improved quality of life without the need for major surgery. We are excited about these great advances in health care and look forward to helping you live pain free.</p>

