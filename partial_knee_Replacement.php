								<h4>PARTIAL KNEE REPLACEMENT (PKR)</h4> 
								<p>A partial knee replacement is an alternative to total knee replacement for some patients with osteoarthritis of the knee. This surgery can be done when the damage is confined to a particular compartment of the knee.<br/><br/>
                                
In a partial knee replacement, only the damaged part of the knee cartilage is replaced with prosthesis.<br/><br/>

Patients with medial, or lateral, knee osteoarthritis can be considered for partial knee replacement. "Medial" refers to the inside compartment of the joint, which is the compartment nearest the opposite knee, while "lateral" refers to the outside compartment farthest from the opposite knee. Medial knee joint degeneration is the most common deformity of arthritis.<br/><br/>

What are the advantages of partial knee replacement over total knee replacement?<br/><br/>

Compared to total knee replacement, partial knee replacement better preserves range of motion and knee function because it preserves healthy tissue and bone in the knee. For these reasons, patients tend to be more satisfied with partial knee replacement compared with total knee replacement.<br/><br/> 

There is also less blood loss during surgery, and knee motion recovers faster with partial knee replacement.<br/><br/>

You can start moving the knee the day after surgery. Your knee function should return rapidly and with less pain than with total knee replacement. You will work with a physical therapist to mobilize the knee while in the hospital and for two to four weeks after discharge. You are usually discharged no later than one or two days after surgery.<br/><br/>

 You should be back to full activity in about 6 to 8 weeks. Impact exercises or jogging/running may not be recommended because the replacement includes a bearing surface that can wear. However, activities like tennis, skiing, and other sports are okay. This, of course, is why partial knee replacement is even being considered â€” to return you to activities you enjoy.
