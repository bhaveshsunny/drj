<p>Shoulder Problems and Treatments</p>
<p>Bursitis or Tendinitis</p>
<p>Bursitis or tendinitis can occur with overuse from repetitive activities, such as swimming, painting, or weight lifting. These activities cause rubbing or squeezing (impingement) of the rotator cuff under the acromion and in the acromioclavicular joint. Initially, these problems are treated by modifying the activity which causes the symptoms of pain and with a rehabilitation program for the shoulder.</p>
<p>Impingement and Partial Rotator Cuff Tears</p>
<p>Impingement and partial tear of the rotator cuff.</p>
<p>Partial thickness rotator cuff tears can be associated with chronic inflammation and the development of spurs on the underside of the acromion or the acromioclavicular joint.</p>
<p>The conservative nonsurgical treatment is modification of activity, light exercise, and, occasionally, a cortisone injection. Nonsurgical treatment is successful in a majority of cases. If it is not successful, surgery often is needed to remove the spurs on the underside of the acromion and to repair the rotator cuff.</p>
<p>Full-Thickness Rotator Cuff Tears</p>
<p>Full-thickness tear of the rotator cuff.</p>
<p>Full-thickness rotator cuff tears are most often the result of impingement, partial thickness rotator cuff tears, heavy lifting, or falls. Nonsurgical treatment with modification of activity is successful in a majority of cases.</p>
<p>If pain continues, surgery may be needed to repair full- thickness rotator cuff tears. Arthroscopic techniques allow shaving of spurs, evaluation of the rotator cuff, and repair of some tears.</p>
<p>Both techniques require extensive rehabilitation to restore the function of the shoulder.</p>
<p>Instability</p>
<p>Instability of the shoulder.</p>
<p>Instability occurs when the head of the upper arm bone is forced out of the shoulder socket. This can happen as a result of sudden injury or from overuse of the shoulder ligaments.</p>
<p>The two basic forms of shoulder instability are subluxations and dislocations. A subluxation is a partial or incomplete dislocation. If the shoulder is partially out of the shoulder socket, it eventually may dislocate. Even a minor injury may push the arm bone out of its socket. A dislocation is when the head of the arm bone slips out of the shoulder socket. Some patients have chronic instability. Shoulder dislocations may occur repeatedly.</p>
<p>Patients with repeat dislocation usually require surgery. Open surgical repair may require a short stay in the hospital. Arthroscopic surgical repair is often done on an outpatient basis. Following either procedure, extensive rehabilitation, often including physical therapy, is necessary for healing.</p>
