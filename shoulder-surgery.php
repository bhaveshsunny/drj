<p>Anatomy of the shoulder joint.</p>
<p>The shoulder is a ball-and-socket joint. It is made up of three bones: the upper arm bone (humerus), shoulder blade (scapula) and collarbone (clavicle).</p>
<p>The ball at the top end of the arm bone fits into the small socket (glenoid) of the shoulder blade to form the shoulder joint (glenohumeral joint).</p>
<p>The socket of the glenoid is surrounded by a soft-tissue rim (labrum).</p>
<p>A smooth, durable surface (articular cartilage) on the head of the arm bone, and a thin inner lining (synovium) of the joint allows the smooth motion of the shoulder joint.</p>
<p>The upper part of the shoulder blade (acromion) projects over the shoulder joint. One end of the collarbone is joined with the shoulder blade by the acromioclavicular (AC) joint. The other end of the collarbone is joined with the breastbone (sternum) by the sternoclavicular joint.</p>
<p>The joint capsule is a thin sheet of fibers that surrounds the shoulder joint. The capsule allows a wide range of motion, yet provides stability.</p>
<p>The rotator cuff is a group of muscles and tendons that attach your upper arm to your shoulder blade. The rotator cuff covers the shoulder joint and joint capsule.</p>
<p>The muscles attached to the rotator cuff enable you to lift your arm, reach overhead, and take part in activities such as throwing or swimming.</p>
<p>A sac-like membrane (bursa) between the rotator cuff and the shoulder blade cushions and helps lubricate the motion between these two structures.</p>


