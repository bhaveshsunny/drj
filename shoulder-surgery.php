<p>Fractured Head of the Humerus (Arm Bone), or Proximal Humerus Fracture</p>
<p>Fractured head of the humerus.</p>
<p>A fractured head of the humerus is a common result of falls on an outstretched arm, particularly by older people with osteoporosis. If fragmented or displaced, it may require open surgical repair and possibly replacement with an artificial joint (prosthesis).</p>
<p>Osteoarthritis and Rheumatoid Arthritis</p>
<p>Arthritis of the shoulder.</p>
<p>Osteoarthritis and rheumatoid arthritis can destroy the shoulder joint and surrounding tissue. They can also cause degeneration and tearing of the capsule or the rotator cuff. Osteoarthritis occurs when the articular surface of the joint wears thin. Rheumatoid arthritis is associated with chronic inflammation of the synovium lining which can produce chemicals that eventually destroy the inner lining of the joint, including the articular surface.</p>
