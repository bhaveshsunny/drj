<p>Orthopaedic Evaluation</p>
<p>The orthopaedic evaluation of your shoulder consists of three components:</p>
<p>A medical history to gather information about current complaints; duration of symptoms, pain and limitations; injuries; and past treatment with medications or surgery.</p>
<p>A physical examination to assess swelling, tenderness, range of motion, strength or weakness, instability, and/or deformity of the shoulder.</p>
<p>Diagnostic tests, such as X-rays taken with the shoulder in various positions. Magnetic resonance imaging (MRI) may be helpful in assessing soft tissues in the shoulder. Computed tomography (CT) scan may be used to evaluate the bony parts of the shoulder.</p>
<p>Your orthopaedic surgeon will review the results of your evaluation with you and discuss the best treatment. You and your doctor may agree that surgery is the best treatment option. He or she will explain the potential risks and complications that may occur. Your doctor may discuss donating your own blood to be used if needed during surgery.</p>
<p>Some surgical procedures require hospitalization for a number of days. Your doctor may discuss planning for the period after surgery. You may need to either stay in an extended care facility or have someone help you when you return home.</p>
<p>Preparing for Surgery</p>
<p>No food or drink after midnight before surgery.</p>
<p>Discuss with your doctor what to do about medications taken in the morning.</p>
<p>An hour before surgery, you will be assessed in the preoperative area by a nurse anesthetist or anesthesiologist.</p>
<p>Types of Surgical Procedures</p>
<p>You may be given the option to have an arthroscopic procedure or an open surgical procedure.</p>
<p>Arthroscopy</p>
<p>Arthroscopy allows the orthopaedic surgeon to insert a pencil-thin device with a small lens and lighting system into tiny incisions to look inside the joint. The images inside the joint are relayed to a TV monitor, allowing the doctor to make a diagnosis. Other surgical instruments can be inserted to make repairs, based on what is with the arthroscope. Arthroscopy often can be done on an outpatient basis. According to the American Orthopaedic Society for Sports Medicine, more than 1.4 million shoulder arthroscopies are performed worldwide each year.</p>
<p>Open Surgery</p>
<p>Open surgery may be necessary and, in some cases, may be associated with better results than arthroscopy. Open surgery often can be done through small incisions of just a few inches.</p>
<p>Recovery and rehabilitation is related to the type of surgery performed inside the shoulder, rather than whether there was an arthroscopic or open surgical procedure.</p>
