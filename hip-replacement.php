                                 <h4>Total Hip Replacement (THR or Total Hip Arthroplasty)</h4>

                                 <p>Hip joint replacement is a medical surgery or procedure which discards the injured joint and  replaced  with a healthy one. A joint is the location where more than two bones join or merge such as the knee, hip, and shoulder. Our  hospital is located in the happening city of  Hyderabad city , providing wide range of  services for Joint Replacement surgery. Our hospital is a renowned hospital for hip replacement surgery in India.</p>

                                 <p>Hip Replacement is a procedure when other pain management therapies  fail to pacify the pain associated with the arthritis of the Hip. Hip replacement is a surgical procedure where the defective hip joint is recouped with prosthetic implant. Such hip  joint replacement surgeries are performed to reduce the  pain  arising due to arthritis or severe physical joint damage which constitutes as a  part of the hip fracture. Once the  surgery is performed the individual  can perform all the daily activities with much ease. Total Hip replacement surgery is also referred to  as Total Hip arthroplasty.</p>

                                 <h4>Why THR  (Total Hip Replacement)</h4>

                                 <p>Osteoarthritis  which occurs in the hip joint is a reason for people to opt for Hip Replacement surgery. Hip Replacement is the best option, if persistent pain occurs, loss of motion or deformity of hip joint. Hip replacement is also performed in people suffering from  hip damage, rheumatoid arthritis or any serious medical condition, such as a bone tumor or bone loss as a result of inadequate blood supply (avascular necrosis).</p>

                                 <h4>Signs of an individual opting for  hip replacement:</h4>

                                 <ol>
                                    <li>Due to severe pain the person cannot sleep.</li>
										 <li>Gets no relief even after taking pain medications.</li>
										 <li>Finds it troublesome to climb the  stairs. </li>
										 <li>Having trouble  to stand after prolonged sitting.</li>
										<li>Facing   difficulty in walking, due to the severe pain.</li>
										
                                 </ol>

                                 <p>Previously Hip replacement was required when individuals crossed 60 years and above. But the  technical advancements  in medical technology, has increased the bone strength which makes it durable and long lasting, however artificial joints  are easily available which are suitable for active people. However, individuals need another surgery replacement  after  15 to 20 years when the replace begins to be worn out.</p>

                                 <h4>Preparing for THR</h4>

                                 <p>Prior to the total hip replacement surgery, the surgeon  will examine and evaluate the patient's physical condition. The hip replacement surgeon  thoroughly checks the patient’s medical history  to make sure that the patient is fit for surgery. The surgeon physically screens the hip, with more focus on the movement  in the joints and  checks the strength of the muscles in the hip area. Blood tests and  X-ray  reports  are  general medical procedures that are performed.</p>

