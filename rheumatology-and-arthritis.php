								<p>People suffer a lot with Rheumatoid arthritis (RA) where it is one of the most paralyzing types of joints. Of course, RA remains a serious disease and one that can vary widely in symptoms (what you feel) and outcomes. At present, Rheumatologists have many advanced treatments that hit the inflammation that RA causes. These treatments made possible to stop or slow the growth of the damage. </p>
                      
								<h4>What causes RA?</h4>
								<p>RA is an autoimmune disease. Your immune system is attacked by foreigners in your body, like viruses and bacteria that creates inflammation. In this disease, an immune system badly sends inflammation to your own healthy tissue causing a lot of pain and swelling in the joints. If the inflammation remains present for a long period, it can cause injury to the entire joint. This injury typically cannot be reversed once it occurs. There is an evidence that it can be occurred due to genetic disorders also.</p>
                     
								<h4>Diagnosis of Rheumatoid arthritis:</h4>
								<p>Rheumatic Arthritis is identified by analyzing blood test results, joints and organs, and reviewing x-ray. Our surgeons perform blood tests to find for antibodies in RA. Antibodies are the proteins that help to fight against foreign substances called antigens.  Some people have no antibodies present in their body, this is called as a false positive result. Symptoms of RA can be very gentle that makes the diagnosis more difficult. A rheumatologist is a physician with experience and knowledge in providing the correct diagnosis of RA and to prescribe a treatment plan.</p>
                              
								<h5>Abnormal blood tests commonly found in RA includes:</h5>
								<p>Anemia (less count of red blood cells), Rheumatoid factor (an antibody, or a blood protein, found in 80% of patients), Elevated erythrocyte sedimentation rate (a blood test, that confirms the amount of inflammation in the joints).  </p>
								<p>X-rays are helpful in detecting RA at the early stages of the disease. So that these X-rays are further useful to observe whether the disease is progressing or not. In other cases, MRI and ultrasound scan are also used to judge the severity of RA.</p>
                        
