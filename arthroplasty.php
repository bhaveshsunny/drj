								<h4>PARTIAL KNEE REPLACEMENT</h4>
								<p>Partial knee replacement ( unicompartmental knee replacement) is a procedure in which only a portion of the knee is resurfaced. This procedure is an alternative to total knee replacement for patients whose disease is limited to just one area of the knee.</p>
								<p>In knee osteoarthritis, the cartilage protecting the bones of the knee slowly wears away. This can occur throughout the knee joint or just in a single area of the knee.</p>
								<p>Advanced osteoarthritis that is limited to a single compartment may be treated with a unicompartmental knee replacement. More than 25% of patients with severe knee arthritis may be appropriate candidates for this type of surgery.  During this procedure, the damaged compartment is replaced with metal and plastic. The healthy cartilage and bone, as well as all of the ligaments are preserved. </p>
								<p>Due to technological advances, more people with knee arthritis are now able to benefit from partial knee replacement.  The life of a partial knee replacement is almost comparable to that of a total knee replacement. On average, there 10-year survival rate of 95% and 20 year survival rate of 80%.</p>
								<p>This is a minimally invasive procedure is an alternative option for arthritis patients in whom only one part of the knee has been affected. </p>
								<p>Partial replacements are becoming more attractive to patients due to:</p>
								<ul>
								<li>Higher patient satisfaction</li>
								<li>Preserving more normal knee motion</li>
								<li>Less blood loss during surgery</li>
								<li>Faster recovery time</li>

								</ul>
								
								
								<p>With proper patient selection, modern unicompartmental knee replacements have demonstrated excellent medium- and long-term results in both younger and older patients.</p>
								
								
								
								
								
