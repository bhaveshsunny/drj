								<h4>STEP TOWARDS OUR SOPHISTICATED SERVICES</h4> 
								<h4>Anthroplasty:</h4> 
								<p>Arthroplasty is a surgical procedure to restore the function of a joint. A joint can be restored by resurfacing the bones. An artificial joint (called prosthesis) may also be used.</p>
								<p>It is performed to relieve pain and restore range of motion by realigning or reconstructing a dysfunctional joint.</p>
								<p>The goal of arthroplasty is to restore the function of a stiffened synovial joint and relieve pain. As a surgical procedure, it is usually performed when medical treatment has not improved function in the affected joint.</p>
								<p>In recent years, joint replacement has become the operation of choice for most chronic knee and hip problems, particularly because of advances in the type and quality of prostheses (artificial joints). Elbow, shoulder, ankle, and finger joints are more likely to be treated with joint resection or interposition reconstruction.</p>
								<p>Arthroplasty is performed on people suffering from severe pain and disabling stiffness</p>
								<h4>CONDITIONS </h4>
								<p>Osteoarthritis (OA), a degenerative joint disease, is the most common condition causing joint destruction with pain and impaired movement. </p>
								<p><strong>Other causes </strong></p>
								<ul>
								<li>Rheumatoid arthritis (RA), </li>
								<li>Hemophilia</li>
								 <li>sinusitis</li>
								<li>Rare bone diseases, which are all known to destroy cartilage. </li>
								</ul>
								<h4>MEDICAL TREATMENTS</h4>
								
								<ul>
								<li>Anti-inflammatory medicines</li>
								<li>Pain medicines</li>
								<li>Limiting painful activities</li>
								<li>Assistive devices for walking (such as a cane)</li>
								<li>Physical therapy</li>
								<li>Cortisone injections into a knee joint</li>
								<li>Viscosupplementation injections (to add lubrication into the joint to make joint movement less painful)</li>
								<li>Weight loss (for obese people)</li>
								<li>Exercise and conditioning</li>
								</ul>
								
								<p>Joint replacement is usually reserved for older patients, because of the limited longevity of benefits. The younger the patient, the greater the reliance on medical treatment.</p>
