								<h4>TOTAL HIP REPLACEMENT</h4>
								<p>The hip is one of the body's largest joints. </p>
								 <p>The most common cause of chronic hip pain and disability is arthritis. Osteoarthritis, rheumatoid arthritis, and traumatic arthritis are the most common forms of this disease.</p>
								 <p>In a total hip replacement (also called total hip arthroplasty), the damaged bone and cartilage is removed and replaced with prosthetic components.</p>
								 <p><strong>TYPES</strong></p>
								 <ul>
								 <li>Cemented</li>
								<li>Uncemented</li>
								<li>Hybrid</li>
								</ul>
								
								<p>There are different bearing options and suited to your requirements by the surgeon. Modern hip replacement materials and bearings are very durable and reliable and rarely break. A good implant and bearing is the key to long-term success of your hip in addition of course to the quality of surgery.</p>
								<p>There are no absolute age or weight restrictions for total hip replacements.</p>
								<p>Recommendations for surgery are based on a patient's pain and disability, not age. Most patients who undergo total hip replacement are age 50 to 80, but orthopaedic surgeons evaluate patients individually. Total hip replacements have been performed successfully at all ages, from the young teenager with juvenile arthritis to the elderly patient with degenerative arthritis and facilitating early mobilization.</p>
								<p>There are in-house physiotherapists to provide pre-op and early post-op care and mobilization and also provide at-home services.</p>
								<p>Total hip arthroplasties are also performed for complex cases leading to good results like:</p>
								
								<ul>
								<li>Post-traumatic arthritis of the hip after acetabular fractures</li>
								<li>Heterotopic ossification of the hip joint</li>
								<li>Ankylosing spondylitis</li>
								<li>Revision for failed total hip arthroplasties</li>
									</ul>
								
								<p><strong>IMPLANT ADVANCES</strong></p>
								<p>Total knee replacement, or total knee arthroplasty, is a surgical procedure in which diseased parts of the knee joint are replaced with artificial parts (prostheses)</p>
								<p>The primary goal is relief of significant, disabling pain caused by severe arthritis.</p>
								<p>Knee replacement is done when other treatments (weight loss, exercise/physical therapy, medicines, and injections) have failed to relieve arthritis-associated knee pain.</p>
								<p>Knee replacement facilitates to relieve pain, improve quality of life, and maintain or improve knee function.</p>
								<p>The procedure can be performed on people of all ages, with the exception of children, whose bones are still growing. </p>
								<p>It is important to have significant pain and/or disability prior to considering this procedure. It is recommended to delay knee replacement until it is absolutely necessary and adviced by orthopedic surgeon after thorough evaluation because the replacement parts can break down over time and lead to increased revision rates. </p>
								<p><strong>ALTERNATIVES TO KNEE REPLACEMENT</strong></p>
								<p>Nonsurgical treatment</p>
								<p><strong>Weight loss.</strong> The knee sees about four pounds of pressure for each pound of body weight, so even a small amount of weight loss (eg, 10 to 15 lbs) can lead to reduced pain.</p>
								<p><strong>Exercise/physical therapy.</strong>  Strengthening the muscles around the knee help take pressure off the knee. Motion of the joint helps to keep it from getting stiff.</p>
								<p><strong>Medications, including over-the-counter and prescription.</strong> These include pain relievers such as acetaminophen and antiinflammatory drugs such as ibuprofen or naproxen. Patients should discuss use of these medications with their primary care provider and pharmacist to be sure the risk of side effects is acceptably low</p>
								<p>Knee bracing or shoe inserts, both of which may help align the knee and balance the weight on the joint.</p>
								<p>Injections, either with a cortisone-like drug or a hyaluronic acid derivative. </p>
								
